#include "MappingCollection.h"

#include "osg\TransferFunction"

//============================================================================
MappingCollection::MappingCollection(int numBins) : m_numBins(numBins)
{
}

//============================================================================
MappingCollection::~MappingCollection(void)
{
  std::vector<osg::TransferFunction1D*>::iterator it;
  it = m_mappingList.begin();
  while (it != m_mappingList.end())
  {
    delete *it;
    it++;
  }
}
